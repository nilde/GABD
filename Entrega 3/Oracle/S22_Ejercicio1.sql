CREATE MATERIALIZED VIEW V1_MOSTRES
AS SELECT D.NOM, V.ID, V.VECTORVALUES, V.CLASE
FROM DATASETS D, VECTOR V
where V.ID_DATASETS = D.NOM
ORDER BY D.NOM;

--VISTA 2:
CREATE MATERIALIZED VIEW V2_EXPERIMENTS AS
SELECT E.CONF, E.REPETICIO, E.METHOD, E.M, E.K E.SIGMA 
FROM EXPERIMENTS E

--VISTA 3:
CREATE MATERIALIZED VIEW V3_OUTLIERS AS
SELECT D.NOM, E.CONFIGURATION, E.REPETICIO, E.ID, V.VECTORVALUES
FROM DATASETS D, EXPERIMENTS E, VECTOR V
WHERE E.ID = V.ID